    public Configs(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();
        PrintService[] pservices = PrinterJob.lookupPrintServices();
        
        if (pservices.length > 0) {
            for (PrintService ps : pservices) {
                impressora.addItem(ps);
                if(ps.getName().equalsIgnoreCase(IMPRESSORA)){
                    impressora.setSelectedItem(ps);
                }
            }
        }
        intervaloAlerta.setValue(INTERVALOALERTA);
        intervaloTabela.setValue(INTERVALOTABELA);
        Config.load();
        
        GraphicsEnvironment ge = GraphicsEnvironment.getLocalGraphicsEnvironment();
        Rectangle tela = ge.getMaximumWindowBounds();
        Dimension janela = this.getSize();
        int sheight = (tela.height/2) - (janela.height/2);
        int swidth = (tela.width/2) - janela.width/2;
        this.setLocation(swidth, sheight);
    }

